
package ders1;

public class HesapMakinesi {
    public static void main(String[] args){
        for(int i = 1 ; i <= 5 ; i++){
            for(int x = 1 ; x <= 5 ; x++){
                System.out.println(i + " * " + x + " = " + (i * x));              
            }
            System.out.println(" -------------------------- ");              

        }
    }    
}
