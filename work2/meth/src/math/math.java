
package math;

public class math {

    public static void main(String[] args) {
            System.out.println(Math.abs(-6.7));            //Mutlak değerini bulur. 
            System.out.println(Math.ceil(8.1));            //Bir büyük sayıya tamamlar.
            System.out.println(Math.floor(8.1));           //Bir küçük sayıya tamamlar.
            System.out.println(Math.max(5, 0));            //Max değeri döndürür.
            System.out.println(Math.min(5, 0));            //Min değeri döndürür.
            System.out.println((int)(Math.random() * 10)); // 0 - 9 arası sayılar üretir.
            System.out.println(Math.round(8.6));           //Yakın olan değerini döndürür.
            System.out.println(Math.pow(3, 2));            //Üssünü alır.
            System.out.println(Math.sin(30));              //Sin değerini alır.
            System.out.println(Math.cos(30));              //Cos değerini alır.
            System.out.println(Math.sqrt(2025));           //Karakökünü döndürür.
    }
    
}
